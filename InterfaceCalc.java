    private void TratarNumeroPressionado(String numero){
        if (digitou == false){
            VisorCalculadora.setText(numero);
            digitou = true;
        }else{
            String visor = VisorCalculadora.getText();
            VisorCalculadora.setText(visor + numero);
        }
        }
    private void tratarOperacaoPressionada(int operacaoDesejada){
        operacao = operacaoDesejada;
        numeroDigitado = Integer.parseInt(VisorCalculadora.getText());
        digitou = false;
    }
    private void TratarIgualPressionado(){
        switch(operacao){
            case 1:
                    total = numeroDigitado + Double.parseDouble(VisorCalculadora.getText());
                    break;
            case 2:
                    total = numeroDigitado - Double.parseDouble(VisorCalculadora.getText());
                    break;
            case 3:
                    total =  total = numeroDigitado * Double.parseDouble(VisorCalculadora.getText());
                    break;
            case 4:
                    total =  total = numeroDigitado / Double.parseDouble(VisorCalculadora.getText());
                    break;
        }
            VisorCalculadora.setText(Double.toString(total));
    }
 
